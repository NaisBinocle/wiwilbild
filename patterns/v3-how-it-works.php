<script>
(() => {
	const steps = document.querySelectorAll('.wwb-v3-step');
	if (!steps.length) return;
	const RANGE = 400;
	const MIN_SCALE = 0.92;
	const update = () => {
		steps.forEach((step, i) => {
			const next = steps[i + 1];
			if (!next) { step.style.transform = ''; return; }
			const pinTop = parseFloat(getComputedStyle(step).top) || 0;
			const rect = step.getBoundingClientRect();
			const stuck = rect.top <= pinTop + 1;
			if (!stuck) { step.style.transform = ''; return; }
			const nextPin = parseFloat(getComputedStyle(next).top) || 0;
			const dist = next.getBoundingClientRect().top - nextPin;
			const progress = Math.max(0, Math.min(1, dist / RANGE));
			const scale = MIN_SCALE + (1 - MIN_SCALE) * progress;
			step.style.transform = 'scale(' + scale.toFixed(3) + ')';
		});
	};
	let raf = 0;
	const schedule = () => { if (!raf) raf = requestAnimationFrame(() => { raf = 0; update(); }); };
	window.addEventListener('scroll', schedule, { passive: true });
	window.addEventListener('resize', schedule);
	update();
})();
</script>
